from enum import Enum

class AIResultType(Enum):
    """
    Enum to classify the type of AI result.
    
    Distinguishes between different types of stored AI artifacts:
    - TOOL: Result from a tool execution (e.g., database query, API call)
    - AGENT_LOG: Log of agent's internal thought process or reasoning
    - AGENT_RESPONSE: Final response generated by the agent
    """
    TOOL = "tool"
    AGENT_LOG = "agent_log"
    AGENT_RESPONSE = "agent_response"

    def __repr__(self) -> str:
        return f"AIResultType.{self.name}"
